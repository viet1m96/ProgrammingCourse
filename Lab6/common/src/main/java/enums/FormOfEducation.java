package enums;

/**
 * The {@code FormOfEducation} enum represents the different forms of education available.
 * It defines the possible education forms that can be used in the application.
 */
public enum FormOfEducation {

    /**
     * Represents distance education.
     */
    DISTANCE_EDUCATION,

    /**
     * Represents full-time education.
     */
    FULL_TIME_EDUCATION,

    /**
     * Represents evening classes.
     */
    EVENING_CLASSES
}
