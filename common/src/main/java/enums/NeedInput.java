package enums;

/**
 * The {@code NeedInput} enum represents whether a command requires user input or not.
 * It defines the possible states for input requirements.
 */
public enum NeedInput {

    /**
     * Indicates that the command requires user input.
     */
    NEED_INPUT,

    /**
     * Indicates that the command does not require user input.
     */
    NO_NEED_INPUT,
}
