package enums;

/**
 * The {@code Semester} enum represents the different semesters of an academic year.
 * It implements the {@code Comparable} interface to allow for sorting of semesters.
 */
public enum Semester implements Comparable<Semester> {

    /**
     * Represents the first semester.
     */
    FIRST,

    /**
     * Represents the second semester.
     */
    SECOND,

    /**
     * Represents the fifth semester.
     */
    FIFTH,

    /**
     * Represents the sixth semester.
     */
    SIXTH,

    /**
     * Represents the seventh semester.
     */
    SEVENTH
}