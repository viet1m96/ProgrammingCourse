package enums;

/**
 * The {@code TypeOfGrp} enum represents the different types of fields or attributes that can be grouped or sorted.
 * It defines the possible grouping types used for data organization and manipulation.
 */
public enum TypeOfGrp {

    /**
     * Represents the ID field.
     */
    ID,

    /**
     * Represents a String field (e.g., name, description).
     */
    STRING,

    /**
     * Represents the X coordinate field.
     */
    X,

    /**
     * Represents the Y coordinate field.
     */
    Y,

    /**
     * Represents the count field.
     */
    COUNT,

    /**
     * Represents the form of education field.
     */
    EDU,

    /**
     * Represents the semester field.
     */
    SEMESTER
}
