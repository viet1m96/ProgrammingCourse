package enums;

/**
 * The {@code Color} enum represents a set of predefined colors.
 * It defines the possible color values that can be used in the application.
 */
public enum Color {

    /**
     * Represents the color red.
     */
    RED,

    /**
     * Represents the color black.
     */
    BLACK,

    /**
     * Represents the color blue.
     */
    BLUE,

    /**
     * Represents the color orange.
     */
    ORANGE,

    /**
     * Represents the color brown.
     */
    BROWN
}
